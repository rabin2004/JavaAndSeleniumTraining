package javaBasics;

public class TernaryOperatorConcept {

	public static void main(String[] args) {
		
		int i = 10;
		
		// assign data type = condition? if true: if false;
		int j = i>10? 20:50;
		
		System.out.println(j);
		
		String m = "Rabin";
		
		Boolean n = (m!="Rabin");
		
		Boolean n1 = (m=="Rabin");
		
		System.out.println(n);
		System.out.println(n1);

	}

}
